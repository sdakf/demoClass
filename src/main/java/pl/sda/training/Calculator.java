package pl.sda.training;

public class Calculator {
    public static int adding(int x, int y) {
        int result = x + y;
        return result;
    }
    public static int subtracting(int x, int y) {
        int result = x - y;
        return result;
    }
    public static int multiplying(int x, int y) {
        int result = x * y;
        return result;
    }
}
